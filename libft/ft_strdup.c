#include "./libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		size;
	int		i;

	size = 0;
	i = 0;
	while (s1[size])
		size++;
	str = malloc(sizeof(char) * size + 1);
	if (str == NULL)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
